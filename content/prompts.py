# 스타일 매핑
# ─────────────────────────────────────────
STYLES = {
    "info": {"name": "정보성 글", "prompt": INFO_PROMPT, "desc": "검색자에게 유익한 정보를 주고 문제 해결 방법을 정리하는 글"},
    "buy":  {"name": "구매성 글", "prompt": BUY_PROMPT, "desc": "직접 만들거나 다른 제품을 고민하는 사람을 구매로 전환하는 글"},
    "ad":   {"name": "광고성 글(파워컨텐츠)", "prompt": AD_PROMPT, "desc": "장문 후기 + 업계 문제 + 제품 선택 과정을 담는 파워컨텐츠용 글"},
}


def get_style_guide(style_id="info", word_count=1500):
    """공통 규칙 + 섹션 전용 프롬프트 결합하여 반환

    Args:
        style_id: 글 스타일 ('info', 'buy', 'ad')
        word_count: 목표 글자 수 (기본 1500)
    """
    style = STYLES.get(style_id, STYLES["info"])
    ratio = word_count / 1500
    replacements = {
        "{word_count}": str(word_count),
        "{intro_count}": str(max(80, int(250 * ratio))),
        "{body_count}": str(max(250, int(950 * ratio))),
        "{section_count}": str(max(80, int(220 * ratio))),
        "{conclusion_count}": str(max(70, int(180 * ratio))),
    }
    prompt = style['prompt']
    for key, val in replacements.items():
        prompt = prompt.replace(key, val)
    return f"{COMMON_RULES}\n\n{prompt}"
